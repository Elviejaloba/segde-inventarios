SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
SET DATEFORMAT DMY 
SET DATEFIRST 7 
SET DEADLOCK_PRIORITY -8;
SELECT 
	STA11.COD_ARTICU AS [Cód. Artículo] ,
	STA11.DESCRIPCIO AS [Descripción] ,
	STA11.SINONIMO AS [Sinónimo] ,
	ISNULL(FAMILIA_ART.COD_AGR, '') AS [Cód. familia (Artículo)] ,
	(CASE sta11.base when '' then STA11.COD_ARTICU ELSE STA11.BASE end) AS [Cód. Base / Artículo] ,
	(CASE sta11.base when '' then STA11.DESCRIPCIO ELSE Base.Descripcio end) AS [Desc. Base / Artículo] ,
	sum(ISNULL(CANT_STOCK,0)) AS [Saldo] ,
	gva16.cotiz AS [gva16.cotiz] ,
	round(ISNULL((SELECT TOP 1 (CASE 'BIMONCTE' WHEN 'BIMONCTE' THEN (case when sta12.mon_cte = 1 then PRECIO_REP else (PRECIO_REP * CASE WHEN 0 >0 THEN 0 ELSE GVA16.COTIZ END) end)  ELSE                   (case when sta12.mon_cte = 0 then PRECIO_REP else (PRECIO_REP / CASE WHEN 0 >0 THEN 0 ELSE GVA16.COTIZ END) end) END )  AS PRECIO FROM STA12	WHERE	STA12.COD_ARTICU = STA11.COD_ARTICU) ,0), ISNULL((SELECT PRECIOS FROM TGANUM), 2)) AS [Costo] ,
	SUM(ISNULL(CANT_STOCK,0) * ISNULL(ROUND((CASE 'BIMONCTE' WHEN 'BIMONCTE' THEN (case when sta12.mon_cte = 1 then PRECIO_REP else (PRECIO_REP * CASE WHEN 0 >0 THEN 0 ELSE GVA16.COTIZ END) end)  ELSE                   (case when sta12.mon_cte = 0 then PRECIO_REP else (PRECIO_REP / CASE WHEN 0 >0 THEN 0 ELSE GVA16.COTIZ END) end) END ) ,PRECIOS) ,0)) AS [Saldo Valorizado] 
FROM 
STA11 
LEFT JOIN STA19 ON STA11.COD_ARTICU = STA19.COD_ARTICU 
LEFT JOIN GVA16 ON 1=1 
LEFT JOIN STA12 ON STA12.COD_ARTICU = STA11.COD_ARTICU 
LEFT JOIN TGANUM ON 1=1 
LEFT JOIN (SELECT COD_ARTICU, DESCRIPCIO FROM STA11 WHERE USA_ESC = 'B') AS BASE ON (BASE.COD_ARTICU = STA11.BASE) 
LEFT JOIN STA16 ON 1 = 1  
LEFT JOIN STA29 FAMILIA_ART ON SUBSTRING(STA11.COD_ARTICU, 1, STA16.LONG_FAM_A) = FAMILIA_ART.COD_AGR  
LEFT JOIN STA29 GRUPO_ART ON SUBSTRING(STA11.COD_ARTICU, 1, LONG_FAM_A + LONG_GRU_A) = GRUPO_ART.COD_AGR


WHERE 

STA11.Stock = 1	AND STA11.PERFIL <> 'N' 

GROUP BY 
	STA11.COD_ARTICU , STA11.DESCRIPCIO , STA11.SINONIMO , ISNULL(FAMILIA_ART.COD_AGR, '') , (CASE sta11.base when '' then STA11.COD_ARTICU ELSE STA11.BASE end) , (CASE sta11.base when '' then STA11.DESCRIPCIO ELSE Base.Descripcio end) , gva16.cotiz
